def valAndAvg() -> float:
    values = []
    print("Enter 5 values:")
    while len(values) < 5:
        try:
            v = float(input(f"Value {len(values) + 1}: "))
            values.append(v)
        except ValueError:
            print("Please enter a valid number.")
