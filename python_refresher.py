def absoluteDistinctCount(A: List[int]) -> int:
    if not A:
        return 0  # though the problem states non-empty

    count = 1
    prev_abs = abs(A[0])

    for x in A[1:]:
        cur_abs = abs(x)
        if cur_abs != prev_abs:
            count += 1
            prev_abs = cur_abs

    return count

